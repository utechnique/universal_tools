// Constructor creates a borderless window in the center of a display.
StartupWindow::StartupWindow() : Fl_Window(Fl::w() / 2 - skWidth / 2,
                                           Fl::h() / 2 - skHeight / 2,
                                           skWidth, skHeight)
{
	clear_border();

	box = ut::MakeUnique<Fl_Box>(0, 0, skWidth, skHeight);
	box->label("LOADING");
	box->labelsize(48);
	box->type(FL_FLAT_BOX);
	box->show();

	end();
	show();
}

