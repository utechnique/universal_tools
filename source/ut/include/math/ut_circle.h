// 2D circle.
template<typename Scalar = float>
struct Circle
{
	Circle()
	{}

	Circle(Scalar center_x,
	       Scalar center_y,
	       Scalar in_radius) : center(center_x, center_y)
	                         , radius(in_radius)
	{}

	Vector<2, Scalar> center;
	Scalar radius;
};

// Specialized type name function for a circle
template <typename Scalar>
struct Type< Circle<Scalar> >
{
	static inline const char* Name() { return "circle"; }
};

