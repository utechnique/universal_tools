// 3D sphere.
template<typename Scalar = float>
struct Sphere
{
	Sphere()
	{}

	Sphere(Scalar center_x,
	       Scalar center_y,
	       Scalar center_z,
	       Scalar in_radius) : center(center_x, center_y, center_z)
	                         , radius(in_radius)
	{}

	Vector<3, Scalar> center;
	Scalar radius;
};

// Specialized type name function for a sphere
template <typename Scalar>
struct Type< Sphere<Scalar> >
{
	static inline const char* Name() { return "sphere"; }
};

