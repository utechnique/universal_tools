// 2D rectangle.
template<typename Scalar = ut::uint32>
struct Rect
{
	Rect()
	{}

	Rect(Scalar offset_x, Scalar offset_y,
	     Scalar width, Scalar height) : offset(offset_x, offset_y)
	                                  , extent(width, height)
	{}

	Vector<2, Scalar> offset;
	Vector<2, Scalar> extent;
};

