// iif
#define UT_PP_IIF(bit, t, f) UT_PP_IIF_OO(I(bit, t, f))
#define UT_PP_IIF_OO(par) UT_PP_IIF_ ## par

#define UT_PP_IIF_I(bit, t, f) UT_PP_IIF_II(UT_PP_IIF_ ## bit(t, f))
#define UT_PP_IIF_II(id) id

#define UT_PP_IIF_0(t, f) f
#define UT_PP_IIF_1(t, f) t

// if
#define UT_PP_IF(cond, t, f) UT_PP_IF_I(cond, t, f)
#define UT_PP_IF_I(cond, t, f) UT_PP_IIF(UT_PP_BOOL(cond), t, f)

