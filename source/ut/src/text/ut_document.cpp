//----------------------------------------------------------------------------//
// Stream manipulator to write document to the output stream
//    @param doc - text document (Xml, JSon, ..) to be written
//    @param stream - output stream
//    @return - formatted text document
Document& operator << (OutputStream& stream, Document& doc)
{
	// just write a document to the stream
	Optional<Error> write_error = doc.Write(stream);
	if (write_error)
	{
		throw Error(write_error.Get());
	}

	// success
	return doc;
}

//----------------------------------------------------------------------------//
// Stream manipulator to read a document from the input stream
//    @param doc - text document (Xml, JSon, ..) to be written
//    @param stream - output stream
//    @return - formatted text document
Document& operator >> (InputStream& stream, Document& doc)
{
	// get the size of the stream buffer
	Result<size_t, Error> get_size_result = stream.GetSize();
	if (!get_size_result)
	{
		throw Error(get_size_result.GetAlt());
	}

	// get the current position of the cursor
	Result<size_t, Error> get_cursor_result = stream.GetCursor();
	if (!get_cursor_result)
	{
		throw Error(get_cursor_result.GetAlt());
	}

	// read everything up to the end of the stream buffer
	size_t bytes_to_read = get_size_result.Get() - get_cursor_result.Get();
	String text(bytes_to_read);
	Optional<Error> read_error = stream.Read(text.GetAddress(), 1, bytes_to_read);
	if (read_error)
	{
		throw Error(read_error.Move());
	}

	// parse document from the text
	Optional<Error> parse_error = doc.Parse(text);
	if (parse_error)
	{
		throw Error(parse_error.Get());
	}

	// success
	return doc;
}

