// Parses text file
//    @param filename - string with a path to the file
//    @return - ut::Error if encountered an error
Optional<Error> Document::ParseFile(const String& filename)
{
	// open file
	File file;
	Optional<Error> open_file_error = file.Open(filename, file_access_read);
	if (open_file_error)
	{
		return open_file_error;
	}

	// get file size
	Result<size_t, Error> get_size_result = file.GetSize();
	if (!get_size_result)
	{
		return get_size_result.GetAlt();
	}
	size_t file_size = get_size_result.Get();

	// read file content to the string
	String text(file_size);
	Optional<Error> read_error = file.Read(text.GetAddress(), 1, file_size);
	if (read_error)
	{
		return read_error;
	}

	// close the file
	file.Close();

	// parse text
	Optional<Error> parse_error = Parse(text);
	if (parse_error)
	{
		return parse_error;
	}

	// success
	return Optional<Error>();
}

// This operator adds a new node to the document
Document& Document::operator << (const Tree<Node>& node)
{
	if (!nodes.Add(node))
	{
		throw Error(error::out_of_memory);
	}
	return *this;
}

// This operator moves a new node to the document
Document& Document::operator << (Tree<Node>&& node)
{
	if (!nodes.Add(Move(node)))
	{
		throw Error(error::out_of_memory);
	}
	return *this;
}

// This operator reads a node from the document
Document& Document::operator >> (Tree<Node>& node)
{
	// check if cursor is inside the bounds
	if (cursor >= nodes.Count())
	{
		throw Error(error::out_of_bounds);
	}

	// copy the node
	node = nodes[cursor];

	// increment the cursor
	cursor++;

	// success
	return *this;
}

// Skips characters until predicate evaluates to true
//    @param text - reference ot the pointer to the current character
//    @param lookup_table - lookup table for skipping encountered characters
void Document::Skip(Reader& txt, const byte* lookup_table)
{
	const char* tmp = txt.Get();
	while (lookup_table[*tmp])
	{
		++tmp;
	}
	txt = tmp;
}

// Generates a string with a specified number of tabs
//    @param num - number of tabs
//    @return - string with the specified number of tabs
String Document::GenerateTabs(uint32 num)
{
	String out;
	for (uint32 i = 0; i < num; i++)
	{
		out.Append('\t');
	}
	return out;
}

