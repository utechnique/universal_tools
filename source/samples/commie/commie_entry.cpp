int UT_MAIN(UT_MAIN_ARG)
{
	try // handle ut::Error exception
	{
		// start log
		ut::Optional<ut::Error> log_error = ut::log.Start("log.txt");

		// main code
		commie::Application app;
	}
	catch(const ut::Error& error)
	{
		ut::log << "\n" << error.GetDesc();
	}

	// finish logging
	ut::log.End();

	// exit
	return 0;
}

